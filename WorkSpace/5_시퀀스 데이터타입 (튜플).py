# 튜플 더하기
t1 = 1,2,3
t2 = 10,20
print(t1+t2)

# 튜플 곱하기
t1 = (1,2,3)
print(t1 * 5)

# 튜플 길이 구하기
t1 = 1,2,3,4,5,6
print(len(t1))


# 패킹 , 언패킹
t1 = 1,2,3,4,5 # 패킹 : 튜플로 포장을 시킴
print(t1)
a,b,c,d,e = (1,2,3,4,5) # 언패킹 : 포장을 시킨거 각각 하나씩 저장시킴
print(a,b,c,d,e)

# 리스트 에서도 언패킹이 가능하다
# 대신 패킹은 불가능하고 튜플에서만 패킹 언패킹 이 가능하다
a,b,c = [10,20,30]
print(a,b,c)









