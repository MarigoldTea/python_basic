# 튜플 (Tuple)
a = tuple()
b= ()
c = (1,2,3)
d= 1,2,3
e = (1,)   # 한개의 요소만 가질때는 요소 뒤에 , 꼭 들어가야함
f = (1,2,3,('a','b'),4)
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(a, type(a))
print(b, type(b))
print(c, type(c))
print(d, type(d))
print(e, type(e))
print(f, type(f))


# 튜플은 값을 수정하거나 삭제하는것이 불가함
